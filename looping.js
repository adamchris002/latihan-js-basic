var n = 5;
var string = ""
for (var i = 1; i <= n; i++) {
    for (var j = 1; j <= i; j++) {
        if (j % 2 == 0) {
            string += "X"
        }
        else {
            string += "#"
        }
    }
    string += "\n"
}

